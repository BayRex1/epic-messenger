app.get('/api/search-users', (req, res) => {
  const { query, currentUserId } = req.query;
  
  if (!query || !currentUserId) {
    return res.json([]);
  }
  
  const currentUser = users.find(u => u.id === currentUserId);
  if (!currentUser) {
    return res.json([]);
  }
  
  const searchTerm = query.toLowerCase().trim();
  let filteredUsers = users.filter(u => 
    u.id !== currentUserId &&
    (u.username.toLowerCase().includes(searchTerm) ||
     u.email.toLowerCase().includes(searchTerm))
  );
  
  // –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –≤–µ—Ä–∏—Ñ–∏—Ü–∏—Ä–æ–≤–∞–Ω - –ø–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–æ–ª—å–∫–æ –≤–µ—Ä–∏—Ñ–∏—Ü–∏—Ä–æ–≤–∞–Ω–Ω—ã—Ö
  if (!currentUser.verified) {
    filteredUsers = filteredUsers.filter(u => u.verified);
  }
  
  // –î–æ–±–∞–≤–ª—è–µ–º –∞–≤–∞—Ç–∞—Ä–∫–∏
  const usersWithAvatars = filteredUsers.map(u => {
    let userAvatar = null;
    if (u.avatar && avatars[u.avatar]) {
      userAvatar = avatars[u.avatar];
    }
    
    return {
      id: u.id,
      username: u.username,
      email: u.email,
      verified: u.verified,
      isDeveloper: u.isDeveloper,
      status: u.status,
      avatar: userAvatar
    };
  });
  
  res.json(usersWithAvatars);
});

